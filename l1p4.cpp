#include <bits/stdc++.h>
using namespace std;

int main() {
    float num1, num2, result;

    cout << "Enter the first floating-point number: ";
    cin >> num1;

    cout << "Enter the second floating-point number: ";
    cin >> num2;

    result = num1 * num2;

    cout << "The product of " << num1 << " and " << num2 << " is: " << result << endl;

    return 0;
}
