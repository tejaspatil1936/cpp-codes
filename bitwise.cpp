#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a = 5 , b = 3;

    cout << "a & b = " << (a & b) << endl;
    cout << "a | b = " << (a | b) << endl;
    cout << "a ^ b = " << (a ^ b) << endl;
    cout << "~a = " << (~a) << endl;
    cout << "~b = " << (~b) << endl;
    cout << "a >> 1 = " << (a >> 1) << endl;
    cout << "b >> 1 = " << (b >> 1) << endl;

    return 0;
}